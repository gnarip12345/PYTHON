# Mathematical Operations

# Addition
sum = 5 + 5
print("\nAddition: ")
print(sum)

# Subtraction
diff = 9 - 5
print("\nSubtraction: ")
print(diff)

# Multiplication
product = 200 * 500
print("\nMultiplication: ")
print(product)

# Division
quotient = 55 / 11
print("\nDivision: ")
print(quotient)

# Integer Division (Floor Division)
integer_div = 13 // 6
print("\nInteger Division: ")
print(integer_div)

# Modulus 
remainder = 99 % 4
print("\nModulus: ")
print(remainder)

# Exponentiation
exp = 2 ** 3
print("\nExponentiation: ")
print(exp)

