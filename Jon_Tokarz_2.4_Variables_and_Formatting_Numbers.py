# Original values in Kilograms
kg_value_1 = 40
kg_value_2 = 65
kg_value_3 = 700
kg_value_4 = 1700

# Conversion value Kilograms to Pounds
kilograms_to_pounds = 2.20462

# Calculate each Kilograms to Pounds
pounds_value_1 = kg_value_1 * kilograms_to_pounds
pounds_value_2 = kg_value_2 * kilograms_to_pounds
pounds_value_3 = kg_value_3 * kilograms_to_pounds
pounds_value_4 = kg_value_4 * kilograms_to_pounds

# Output the results
print(f"{kg_value_1} kilograms is equal to {pounds_value_1} pounds.")
print(f"{kg_value_2} kilograms is equal to {pounds_value_2} pounds.")
print(f"{kg_value_3} kilograms is equal to {pounds_value_3} pounds.")
print(f"{kg_value_4} kilograms is equal to {pounds_value_4} pounds.")





